package com.hsystudio.valtips.data.local

import android.content.Context
import androidx.datastore.preferences.core.booleanPreferencesKey
import androidx.datastore.preferences.core.edit
import androidx.datastore.preferences.preferencesDataStore
import dagger.hilt.android.qualifiers.ApplicationContext
import kotlinx.coroutines.flow.Flow
import kotlinx.coroutines.flow.map
import javax.inject.Inject
import javax.inject.Singleton

private const val DATASTORE_NAME = "valtips_user"
private val Context.dataStore by preferencesDataStore(name = DATASTORE_NAME)

@Singleton
class UserInfoManager @Inject constructor(
    @ApplicationContext private val context: Context
) {
    private object Keys {
        val OnboardingCompleted = booleanPreferencesKey("onboarding_completed")
    }

    // 조회
    fun onboardingCompletedFlow(): Flow<Boolean> =
        context.dataStore.data.map { it[Keys.OnboardingCompleted] ?: false }

    // 저장
    suspend fun setOnboardingCompleted(value: Boolean) {
        context.dataStore.edit { it[Keys.OnboardingCompleted] = value }
    }
}
